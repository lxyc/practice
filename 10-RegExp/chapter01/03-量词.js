// {m,} 至少出现m次
// {m} 出现m次
// ? === {0,1} 出现或者不出现
// + === {1,} 至少出现1次
// * === {0,} 出现任意次，有可能不出现

const str = '123 1234 12345 123456'

// 贪婪匹配，尽可能多的匹配
const regex = /\d{3,}/g
console.log(str.match(regex))

// 惰性匹配，满足条件即可
const regex1 = /\d{3,}?/g
console.log(str.match(regex1))

/**
 * 量词后面加个问号就能实现惰性匹配，以下是所有的惰性匹配情形
 * {m,n}?
 * {m,}?
 * ??
 * +?
 * *?
 */